import java.util.Scanner;
public class Counting {
    
    public static void main(String[] args)
    {
        int num;
        Scanner sc= new Scanner(System.in);
        System.out.println("Enter num");
        num= sc.nextInt();
        for( int i=0; i<=num; i++){
            System.out.println(i);

        }
        
}
        
}        
    